    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        
        
        
        String brandname = txtBrandName.getText();
        if(brandname == null || brandname.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Brand Name can't be empty");
                return;
            }
        
        String manuUYear = txtManuYear.getText();
        if (manuUYear == null || manuUYear.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Please enter the manufacturing year", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtManuYear.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"You need to put valid value", " Warning", JOptionPane.WARNING_MESSAGE);
            return;
            
        }   
        
         String maxCapa = txtMaxCapacity.getText();
        if (maxCapa == null || maxCapa.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Please enter the Maximum Seats Capacity", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtMaxCapacity.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"You need to put valid value", " Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }   
         
          String minCapa = txtMinCapacity.getText();
        if (minCapa == null || minCapa.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Please enter the Minimum Seats Capacity", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtMinCapacity.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"You need to put valid value", " Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } 
         
         String serNum = txtSerialNumber.getText();
        if (serNum == null || serNum.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Please enter the Serial Numbers", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         try {
            Integer.parseInt(txtSerialNumber.getText());
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null,"You need to put valid value", " Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } 
         
         String modNum = txtModelNumber.getText();
        if (modNum == null || modNum.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Please enter the Model Number", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
         
         String avaCity = txtCity.getText();
        if (avaCity == null || avaCity.equals("")) 
        {
        JOptionPane.showMessageDialog(null,"Please enter the Model Number", " Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        if(btnTrue.isSelected()== false && btnFalse.isSelected()== false)
        {
            JOptionPane.showMessageDialog(null,"Please select the Availability", " Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(btnExp.isSelected()== false && btnNonExp.isSelected()== false)
        {
            JOptionPane.showMessageDialog(null,"You'll have to tick the Expiration status", " Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String brandName = txtBrandName.getText();
        int manuYear = Integer.parseInt(txtManuYear.getText());
        int maxSeats = Integer.parseInt(txtMaxCapacity.getText());
        int minSeats = Integer.parseInt(txtMinCapacity.getText());
        int serialNumber = Integer.parseInt(txtSerialNumber.getText());
        String modelNumber = txtModelNumber.getText();
        String availCity = txtCity.getText();
        
        Uber uber = uberCatalog.addUber();
        uber.setBrand(brandName);
        uber.setManufacturedYear(manuYear);
        uber.setMaxSeats(maxSeats);
        uber.setMinSeats(minSeats);
        uber.setSerialNumber(serialNumber);
        uber.setModelNumber(modelNumber);
        uber.setAvailableCity(availCity);
        uber.setAvailable(btnTrue.isSelected());
        uber.setMaintainanceCerti(btnExp.isSelected());
        
       /* Timestamp tP = new Timestamp(System.currentTimeMillis());
        uberCatalog.setLastUpdated(tP);
*/
        JOptionPane.showMessageDialog(null,"Car added successfully!");
        
        txtBrandName.setText("");
        txtCity.setText("");
        txtManuYear.setText("");
        txtMaxCapacity.setText("");
        txtModelNumber.setText("");
        txtMinCapacity.setText("");
        txtSerialNumber.setText("");
        btnnGROUP1.clearSelection();
        btnnGROUP2.clearSelection();
        
    }//GEN-LAST:event_btnCreateActionPerformed

